#include<iostream>
#include<algorithm>
#include<vector>
using namespace std;

//main begin
int main()
{
    int a = 10;
    int b = 20;
    cout << "Value of a before: " << a << endl;
    cout << "Value of b before: " << b << endl;

    // swap values of the variables
    swap(a, b);
    cout << "Value of a now: " << a << endl;
    cout << "Value of b now: " << b << endl;
    getchar();
    return 0;
}
